Eclipse Newcomers FAQ

What is Eclipse?

   Eclipse is an open source community whose projects are focused on
   building an extensible development platform, runtimes and application
   frameworks for building, deploying and managing software across the
   entire software lifecycle. Many people know us, and hopefully love us,
   as a Java IDE but Eclipse is much more than a Java IDE.

   The Eclipse open source community has over 60 open source projects.
   These projects can be conceptually organized into seven different
   "pillars" or categories:
    1. Enterprise Development
    2. Embedded and Device Development
    3. Rich Client Platform
    4. Rich Internet Applications
    5. Application Frameworks
    6. Application Lifecycle Management (ALM)
    7. Service Oriented Architecture (SOA)

   The Eclipse community is also supported by a large and vibrant
   ecosystem of major IT solution providers, innovative start-ups,
   universities and research institutions and individuals that extend,
   support and complement the Eclipse Platform.

   The exciting thing about Eclipse is many people are using Eclipse in
   ways that we have never imagined. The common thread is that they are
   building innovative, industrial strength software and want to use great
   tools, frameworks and runtimes to make their job easier.

What is the Eclipse Foundation?

   The Eclipse Foundation is a not-for-profit, member supported
   corporation that hosts the Eclipse projects. The Foundation provides
   services to run the IT infrastructure, IP due diligence, mentor the
   open source projects during the Eclipse development process and provide
   marketing and business development support for the Eclipse community.

   It is important to realize that the Eclipse Foundation does not
   actually develop the open source code. All of the open source software
   at Eclipse is developed by open source developers, called committers,
   which are volunteered or contributed by organizations and individuals.

How can I get started using Eclipse?

   Most people start by downloading one of the different download
   packages.
   There is also a wealth of books, tutorials, and white papers to help
   you get started. Check out The Official Eclipse FAQ in the Eclipse Wiki
   for more information about all aspects of Eclipse. There are also
   companies available to help with training and consulting services. In
   addition, please feel free to post a question on our forums.

Where do I get support?

   Everyone is free to open bugs and ask questions on our forums. There
   are also companies that offer commercial products built on Eclipse and
   Eclipse distributions. Many of these organizations are able to provide
   commercial level support.

How do I get in contact with other Eclipse users?

   The best way to get in touch with other Eclipse users is via our forums
   and Bugzilla database. This is typically where people ask and answer
   questions about their favourite Eclipse projects. You can also find
   individuals blogging about Eclipse on PlanetEclipse.org.

   If you would like to contact the Eclipse Foundation see our Contact Us
   section.

What license does Eclipse use?

   Eclipse uses the Eclipse Public License (EPL). The EPL is a
   commercially friendly license that allows organizations to include
   Eclipse software in their commercial products, while at the same time
   asking those who create derivative works of EPL code to contribute back
   to the community.

   The commercial-friendly nature of the EPL had been proven over and over
   as hundreds of companies ship Eclipse based products. More information
   about the EPL can be found in our Licensing FAQ.

How do I start an Eclipse open source project?

   The first step in starting an open source project is to familiarize
   yourself with the existing open source projects. There is a lot of
   diverse technology being developed at Eclipse, so it might be actually
   easier to contribute to an existing open source project. If you believe
   you have a unique technology that is suited for Eclipse, then the first
   step is to read the Eclipse development process to ensure that the
   goals of Eclipse align with your interests in creating an open source
   project. The next step is to contact the Eclipse Management
   Organization (EMO) to get some help and advice in drafting your
   proposal. You can contact the EMO via emo at eclipse dot org.

Who is allowed to start an Eclipse open source project?

   Eclipse is an open and transparent community of open source projects.
   We welcome project proposals from all sources. It is important to
   understand that you do not have to be a member of the Eclipse
   Foundation to start or work on an open source project.

